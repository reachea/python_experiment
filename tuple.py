# read only list
num = (1, 2, 3)
print(num)

# len, element, slice

# len
print(len(num))

# element
print(num[1])

# slice
print(num[0:2])


# when using only one element tuple we need to add trailing so there's no conflict with single element and tuple with only a single element
alphabet = ('a')
print(type(alphabet))

alphabet = ('a', )
print(type(alphabet))
